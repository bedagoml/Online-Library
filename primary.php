<p>At the end of the school year students advance to the next grade.Since repetition was banned students still progress to the next grade even though they fail their examinations. Most primary schools are day schools with pupils living at home. Fewer schools at primary level are boarding schools compared to secondary schools. All public primary school pupils sit for the Kenya Certificate of Primary Education examination at the end of the school year in Standard eight.[citation needed] In primary school, students are taught English, Kiswahili and Indigenous language activities.[5]
In January 2003 President Mwai Kibaki re-introduced free primary education which previously existed before the mid 80s when the government adopted cost sharing measures that led to a minor level of school fees charged by primary schools for text books, PTA, and extra curricular activities. Since 2003, education in public schools became free and compulsory ( Kenya Constitution,Article 53, 2010). 
